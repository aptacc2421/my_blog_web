/** 一行里多段颜色（伪 ANSI） */
export type AsciiSeg = { t: string; c: string };
export type AsciiLine = AsciiSeg[];

function seg(t: string, c: string): AsciiSeg {
  return { t, c };
}

/** 整行同色 */
function line(s: string, c: string): AsciiLine {
  return [seg(s, c)];
}

const W = 76;

const pad = (s: string) => s.padEnd(W, " ").slice(0, W);

